        jButtonConexionServidor.setText("DESCONECTAR SERVIDOR");
        jLabelEstadoPrograma.setText("CONECTADO");
        Color color = new Color(153,255,153);
        jLabelEstadoPrograma.setForeground(color);
        // Para establecer el borde
        Border raisedSoftBevel = new SoftBevelBorder(SoftBevelBorder.RAISED);
        jButtonConexionServidor.setBorder(raisedSoftBevel);
        conectado = true;
        recibirInfoServer();
